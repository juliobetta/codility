require 'rspec'

########################################################################################################################
# IMPLEMENTATION #######################################################################################################
########################################################################################################################

module FrogJump
  def self.solution(x, y, d)
    ((y-x)/d.to_f).ceil
  end
end


########################################################################################################################
# TESTS ################################################################################################################
########################################################################################################################

RSpec.describe FrogJump do
  it 'returns the right result' do
    expect(FrogJump.solution(10, 85, 30)).to eq 3
  end
end



