require 'rspec'

###################################################################################################################
# IMPLEMENTATION ##################################################################################################
###################################################################################################################

module CyclicRotation
  def self.solution(a, k)
    a.rotate k*-1
  end
end


###################################################################################################################
# TESTS ###########################################################################################################
###################################################################################################################

RSpec.describe CyclicRotation do

  it 'rotates the array 3 times' do
    expect(CyclicRotation.solution([3,8,9,7,6], 3)).to eq [9,7,6,3,8]
  end

end



